#include <utils.h>

int		ft_strisnum(char *str)
{
	if (*str == '+' || *str == '-')
		str++;
	if (*str == '-' && !*(str + 1))
		return (1);
	if (!(*str >= '0' && *str <= '9') && *str != ' ')
		return (0);
	while (str && *str)
	{
		if (!(*str >= '0' && *str <= '9') && *str != ' ')
			return (0);
		str++;
	}
	return (1);
}
